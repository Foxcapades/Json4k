package io.foxcapades.lib.json4k

/**
 * Json Element Type
 *
 * @since v1.0.0
 */
enum class JsonElementType {
  /**
   * Type "array".
   */
  Array,

  /**
   * Type "boolean".
   */
  Boolean,

  /**
   * Type "integer".
   */
  Integer,

  /**
   * Type "null".
   */
  Null,

  /**
   * Type "number".
   */
  Number,

  /**
   * Type "object".
   */
  Object,

  /**
   * Type "string".
   */
  String,
  ;
}